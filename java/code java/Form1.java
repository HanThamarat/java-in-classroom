import javax.swing.*;
import java.awt.*;
class Form1 
{
	public static void main(String[] args) 
	{
		JFrame f=new JFrame(" Test JFrame ");

	Toolkit size=f.getToolkit();
	Dimension wsize=size.getScreenSize();
	f.setBounds(wsize.width/4,wsize.height/4,wsize.width/2,wsize.height/2);	

	f.setVisible(true);
	f.setResizable(false);
	f.setDefaultCloseOperation(f.EXIT_ON_CLOSE);
	}
}
